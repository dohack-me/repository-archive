def sanitize_url(link: str) -> bool:
    link_details = urlparse(link)
    check = [check_not_localhost(link_details.hostname), check_domain(link), check_scheme(link_details.scheme)]
    if all(check):
        return True
    return False


def visit_url(link: str) -> bytes:
    try:
        res = urllib.request.urlopen(link, timeout=3)
        content = res.read()
        return content if content else b""
    except Exception as e:
        return repr(e).encode()
